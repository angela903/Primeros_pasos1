# Se declaran las variables de entrada 
C = int(input('Ingrese la cantidad de grados celcius a convertir:'))

# Se realiza operatoria para el calculo de los grados Celcius y Fahrenheit
F = 9/5 * C + 32

# Imprime el resultado de los grados Celcius que equivale a los grados Fahrenheit
print(C, 'grados Celcius equivale a', F, 'grados Fahrenheit.')